var MakeTest = function (Qlist, anwserCount) {
    var point = 0;
    Qlist.forEach(element => {
        var x = Math.floor((Math.random() * anwserCount));
        if (x == element) {
            //console.log(x + "," + element + " .");
            point = point + 3;
        } else {
            //console.log(x + "," + element);
            point = point - 1;
        }
    });
    if (point > 0) return true;
    else return false;
}


